#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - function to sum two number
 * @a: intger to add
 * @b: intger to add
 * Return: op_add
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function sub of two number
 * @a: number to sub
 * @b: intger to sub
 * Return: op_sub
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to mul two intger
 * @a: an intger to mul
 * @b: intger to mul
 * Return: op_mul
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to div two number
 * @a: number to div
 * @b: number to div
 * Return: op_div
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - A function that mod of 2 intgers
 * @a: intger to mod
 * @b: intger to mod
 * Return: to op_mod a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
